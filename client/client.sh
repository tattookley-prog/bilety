#!/usr/bin/env bash
# =============================================================================
# client/client.sh — Загрузчик скриптов bilety (публичный файл).
#
# Этот скрипт НЕ содержит логики билетов и никаких секретов.
# Он только запрашивает логин/пароль и номер билета, отправляет POST-запрос
# на сервер владельца и выполняет полученный скрипт в памяти.
#
# Использование:
#   bash client.sh               — интерактивный режим
#   bash client.sh <ticket_id>   — билет передаётся аргументом (например: 1 или 12)
#
# Настройка:
#   Укажите адрес сервера через переменную окружения:
#     export BILETY_SERVER_URL="https://<your-username>.pythonanywhere.com"
#   Если переменная не задана, скрипт запросит URL интерактивно.
# =============================================================================
set -euo pipefail

RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'; CYAN='\033[0;36m'; NC='\033[0m'
info()  { echo -e "${CYAN}[INFO]${NC}  $*"; }
ok()    { echo -e "${GREEN}[OK]${NC}    $*"; }
warn()  { echo -e "${YELLOW}[WARN]${NC}  $*"; }
error() { echo -e "${RED}[ERROR]${NC} $*" >&2; }

if ! command -v curl &>/dev/null; then
    error "curl не найден. Установите curl и повторите попытку."
    exit 1
fi

# ---------------------------------------------------------------------------
# Адрес сервера (из переменной окружения или интерактивный ввод)
# ---------------------------------------------------------------------------
SERVER_URL="${BILETY_SERVER_URL:-}"
if [[ -z "$SERVER_URL" ]]; then
    read -rp "Введите URL сервера (например https://user.pythonanywhere.com): " SERVER_URL
fi
SERVER_URL="${SERVER_URL%/}"   # убираем завершающий слэш

# Проверяем, что URL использует HTTPS (защита от передачи учётных данных по HTTP)
if [[ "$SERVER_URL" != https://* ]]; then
    error "URL сервера должен начинаться с https://. Отказ от отправки учётных данных по незащищённому соединению."
    exit 1
fi

# ---------------------------------------------------------------------------
# Учётные данные
# ---------------------------------------------------------------------------
echo
read -rp  "Логин: " LOGIN
read -rsp "Пароль: " USER_SECRET
echo   # перевод строки после скрытого ввода

# ---------------------------------------------------------------------------
# Выбор билета
# ---------------------------------------------------------------------------
TICKET="${1:-}"
if [[ -z "$TICKET" ]]; then
    echo
    echo "Доступные билеты:"
    echo "  1  — Samba AD DC"
    echo "  2  — RAID 5"
    echo "  3  — NFS"
    echo "  4  — NTP (chrony)"
    echo "  5  — Ansible"
    echo "  6  — Docker + Wiki"
    echo "  7  — Проброс портов"
    echo "  8  — Moodle"
    echo "  9  — MariaDB (Moodle)"
    echo "  10 — nginx reverse proxy"
    echo "  11 — sudo для группы hq"
    echo "  12 — DNS + браузер (ticket12_dns_add_records)"
    echo " 12b — Браузер (ticket12_hqcli_browser)"
    echo
    read -rp "Номер билета: " TICKET
fi

# ---------------------------------------------------------------------------
# Запрос к серверу
# ---------------------------------------------------------------------------
info "Запрос к серверу: ${SERVER_URL}/get ..."

TMP_BODY=$(mktemp)
TMP_HEADERS=$(mktemp)
# Удаляем временные файлы при любом завершении скрипта
trap 'rm -f "$TMP_BODY" "$TMP_HEADERS"' EXIT

# Формируем тело запроса — учётные данные передаются через переменные окружения,
# а не как аргументы командной строки, чтобы они не были видны в `ps aux`.
# Поле для секрета называется "cred" (короткое нейтральное имя).
HTTP_STATUS=$(curl -s -o "$TMP_BODY" -w "%{http_code}" \
    -X POST "${SERVER_URL}/get" \
    --data-urlencode "login=${LOGIN}" \
    --data-urlencode "cred=${USER_SECRET}" \
    --data-urlencode "ticket=${TICKET}" \
    -D "$TMP_HEADERS" \
    2>/dev/null)

BODY=$(cat "$TMP_BODY")

# ---------------------------------------------------------------------------
# Обработка ответа
# ---------------------------------------------------------------------------
case "$HTTP_STATUS" in
    200)
        USES_LEFT=$(grep -i "^X-Uses-Left:" "$TMP_HEADERS" 2>/dev/null \
            | awk '{print $2}' | tr -d '\r' || true)
        ok "Доступ разрешён. Выполнение билета ${TICKET}..."
        [[ -n "$USES_LEFT" ]] && info "Осталось запусков: ${USES_LEFT}"
        echo
        # Выполняем скрипт в памяти — он никогда не сохраняется на диск
        echo "$BODY" | bash
        ;;
    400)
        error "Неверный запрос: ${BODY}"
        exit 1
        ;;
    401)
        error "Неверный логин или пароль."
        exit 1
        ;;
    402)
        warn "Аккаунт не активирован. Произведите оплату и дождитесь подтверждения."
        warn "${BODY}"
        exit 1
        ;;
    403)
        warn "Лимит запусков исчерпан (5 из 5 использованы)."
        warn "Для продления обратитесь к продавцу."
        exit 1
        ;;
    000)
        error "Не удалось подключиться к серверу ${SERVER_URL}."
        exit 1
        ;;
    *)
        error "Сервер вернул неожиданный статус ${HTTP_STATUS}. Ответ: ${BODY}"
        exit 1
        ;;
esac
